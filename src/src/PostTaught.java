package src;

public class PostTaught extends Student {
	private String dept;
	private int fee;
	/**
	 * @param dept the dept to set
	 */
	public void setDept(String dept) {
		this.dept = dept;
	}
	/**
	 * @return the dept
	 */
	public String getDept() {
		return dept;
	}
	/**
	 * @param fee the fee to set
	 */
	public void setFee(int fee) {
		this.fee = fee;
	}
	/**
	 * @return the fee
	 */
	public int getFee() {
		return fee;
	}
	

}
